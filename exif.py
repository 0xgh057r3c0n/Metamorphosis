try:
    with open(photo_path, 'rb') as f:
        image = Image.open(f)
        image.verify() 
        print(Fore.GREEN + "Valid Image!" + Style.RESET_ALL)
except (FileNotFoundError, IOError):
    print(Fore.RED + "Invalid file or file does not exist!" + Style.RESET_ALL)
    exit()

tags = extract_exif_data(photo_path)
if tags:
    print(Fore.CYAN + "\nEXIF Data:" + Style.RESET_ALL)
    for tag, value in tags.items():
        print(f"{tag}: {value}")
else:
    print(Fore.YELLOW + "\nNo EXIF data found." + Style.RESET_ALL)
